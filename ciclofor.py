#este es el ciclo en el cual hace que no se empiece desde 0 sino desde 1
for i in range(1,11):
    print(i)
#este es el ciclo normal del for
for j in range(0,11):
    print(j)
#este es el cilo donde se cuenta hacia atras desde un numero puesto por el usuario
for q in range(80,0,-1):
    print(q)
#este es el ciclo donde se cuenta de dos en dos
for l in range(0,11,2):
    print(l)
